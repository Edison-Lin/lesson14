<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
          <!-- <img :src="avatar+'?imageView2/1/w/80/h/80'" class="user-avatar"> -->
          <img :src="`./images/avatar/${(avatar)?avatar:'pic02.svg'}`" class="user-avatar" :title="name">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <router-link to="/">
            <el-dropdown-item>
              回首頁
            </el-dropdown-item>
          </router-link>
          <el-dropdown-item divided>
            <a style="display:block;" v-bind:href="frontUrl" target="_blank">前台首頁</a>
          </el-dropdown-item>
          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">系統登出</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import { Message } from 'element-ui';

export default {
  data(){
    return {
      //前台URL路徑
      frontUrl:'',
    }
  },
  components: {
    Breadcrumb,
    Hamburger
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar',
      'name',
    ])
  },
  mounted() {
    //電商前台專URL路徑'http://front.edu/lesson-14_5/No_attache/'
    this.frontUrl =this.$store.state.settings.ecPlatForm.ec_Url;
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      let result=await this.$store.dispatch('user/logout')
      if(result.code==20000){
        Message({type:'success',message:result.message});
      }
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    }
  }
}
</script>

